public class TypeCasting {

    public static void main(String[] args) {

        //widening type casting(smoll to large)
        int x = 10;
        double y = x;
        System.out.println(y);


        //Narrowing type casting(Large to smoll)
        double m = 10.5;
        int n = (int) m;
        System.out.println(n);


    }

}
