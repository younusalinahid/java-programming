package Problem_Solving;

public class listNode {

   //2181. Merge Nodes in Between Zeros
    int val;
    listNode next;

    public listNode(){}
    public listNode(int val){
        this.val = val;
    }
    public listNode(int val, listNode next) {
        this.val = val;
        this.next = next;
    }

}
